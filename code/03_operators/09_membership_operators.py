"""
# Membership Operators in Python
Membership operators are used to test if a value is found in a sequence (such as a list, tuple, or string). The two membership operators are:
- `in`    : Returns True if a value is found in the sequence
- `not in`: Returns True if a value is not found in the sequence
"""

# Example usage of membership operators
my_list = [1, 2, 3, 4, 5]  # Ignore this line for this time only
print()
print("Given my_list =", my_list)
print("3 in my_list:", 3 in my_list)  # True
print("6 in my_list:", 6 in my_list)  # False
print("3 not in my_list:", 3 not in my_list)  # False
print("6 not in my_list:", 6 not in my_list)  # True
print()

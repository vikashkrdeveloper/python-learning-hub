"""
# Logical Operators in Python
Logical operators are used to combine conditional statements. The three main logical operators in Python are:

Here are the common logical operators:
- `and` : Returns True if both statements are true
- `or`  : Returns True if one of the statements is true
- `not` : Reverses the result, returns False if the result is true
"""

# Example usage of logical operators

a = 10
b = 3

print()
print(f"Given a = {a} and b = {b}\n")
print("Logical AND (a > 5 and b < 5):", (a > 5) and (b < 5))  # True
print("Logical OR (a > 5 or b > 5):", (a > 5) or (b > 5))  # True
print("Logical NOT (not (a > 5)):", not (a > 5))  # False
print()

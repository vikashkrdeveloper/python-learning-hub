"""
# Comparison (Relational) Operators in Python
Comparison operators are used to compare two values. They return a boolean value (`True` or `False`) based on the comparison.

Here are the common comparison operators:

- `==` : Equal to
- `!=` : Not equal to
- `>`  : Greater than
- `<`  : Less than
- `>=` : Greater than or equal to
- `<=` : Less than or equal to
"""

# Example usage of comparison operators
a = 10
b = 3

print()
print(f"Similar to previous examples, we have a = {a} and b = {b}\n")
print("Equal to (a == b):", a == b)  # False
print("Not equal to (a != b):", a != b)  # True
print("Greater than (a > b):", a > b)  # True
print("Less than (a < b):", a < b)  # False
print("Greater than or equal to (a >= b):", a >= b)  # True
print("Less than or equal to (a <= b):", a <= b)  # False
print()

"""
# Arithmetic Operators
In Python, arithmetic operators are used to perform mathematical operations between numeric values. Here are the common arithmetic operators:

- `+`  : Addition
- `-`  : Subtraction
- `*`  : Multiplication
- `/`  : Division
- `%`  : Modulus (remainder)
- `**` : Exponentiation (power)
- `//` : Floor Division (quotient without remainder)

"""

# Example usage of arithmetic operators
a = 10
b = 3

print("a =", a)
print("b =", b)
print("Addition (a + b):", a + b)  # 13
print("Subtraction (a - b):", a - b)  # 7
print("Multiplication (a * b):", a * b)  # 30
print("Division (a / b):", a / b)  # 3.333
print("Modulus (a % b):", a % b)  # 1
print("Exponentiation (a ** b):", a**b)  # 1000
print("Floor Division (a // b):", a // b)  # 3
print()

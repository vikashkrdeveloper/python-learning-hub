import os

dir_path = "."  # Current directory

# The `listdir` function returns a list of entries in the specified directory
contents = os.listdir(dir_path)

# Print the list of files and directories in the current directory
print("Current directory contents:", contents)

# Print each entry in the directory
print("Contents of the current directory:")
for entry in contents:
    print(entry)

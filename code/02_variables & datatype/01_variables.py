"""
# What is a variable?
## A variable is a named location in memory that stores a value. It acts as a container for data that can be changed during the execution of a program.
## Variables are used to hold information that can be referenced and manipulated throughout a program.
## In Python, you can create a variable by simply assigning a value to a name using the equals sign (=).

# Example of creating and using a variable in Python
x = 10  # Here, 'x' is a variable that holds the integer value 10
y = 5   # 'y' is another variable that holds the integer value 5
"""

a = 1  # a is a variable that holds the constant value 1
b = 2  # b is a variable that holds the constant value 2
c = a + b  # c is a variable that holds the value of a+b which is 3

print(c)  # prints the value of c which is 3

d = c * 2  # d is a variable that holds the value of c*2 which is 6

print(d)  # prints the value of d which is 6
